import re
with open('input_third.txt') as file:
    for line in file:
        ans = list()
        for x in line.split(" "):
            if re.fullmatch(r'\b(\d*)\b', x):
                ans.append(str(4*int(x)**2 - 7))
            else:
                ans.append(x)
        print(' '.join(ans))
